// E60
$('.img_flex div #e60').on('click', () => {
   $('.img_flex div #e60').attr('src', 'img/e60.jpg').hide(1000).delay(1000).show(1000);
   $('#div').show(1000).delay(1000).hide(1000);
   $('#div img').attr('src', 'img/e60.jpg');
});

